@admin.register(Zone)
class ZoneAdmin(admin.ModelAdmin):
    list_display  = ["zone_id", "zone_type", "rack_count_display",
                     "total_volume_display", "created_at"]
    search_fields = ["zone_id", "zone_type"]
    readonly_fields = ["rack_count_display", "total_volume_display",
                       "total_weight_display", "created_at"]
    ordering      = ["zone_id"]

    def rack_count_display(self, obj):
        return obj.rack_count
    rack_count_display.short_description = "Racks"

    def total_volume_display(self, obj):
        return f"{obj.total_volume_cm3:,.0f} cm³"
    total_volume_display.short_description = "Total Volume"

    def total_weight_display(self, obj):
        return f"{obj.total_weight_kg:,.1f} kg"
    total_weight_display.short_description = "Total Max Weight"


