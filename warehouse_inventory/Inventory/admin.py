"""
inventory/admin.py

Django admin registrations for the Inventory app.

Models registered:
  Zone, Rack, Shelf (read-only), Bin (read-only)
  Batch
  Inventory         (read-only — written only via putaway confirmation)
  StockMovement     (read-only — written only by system)
  PurchaseRequest
  PurchaseOrder     (read-only — written only when PR is approved)
  ASN + ASNItem
  GRN + GRNItem
  PutawayPlan

Design decisions:
  - Inventory, StockMovement, PurchaseOrder are read-only in admin
    (system-managed via API flows).
  - Shelf and Bin are read-only (auto-generated by Rack.save()).
  - GRNItem is inline under GRN for context, and also standalone.
  - PutawayPlan is standalone; no inline under GRN (too many rows).
"""

