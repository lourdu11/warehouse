@admin.register(PutawayPlan)
class PutawayPlanAdmin(admin.ModelAdmin):
    list_display  = ["plan_id", "product", "vendor", "batch",
                     "bin", "zone_display",
                     "planned_quantity", "quantity_placed",
                     "completion_pct", "status",
                     "completed_by", "completed_at", "created_at"]
    list_filter   = ["status",
                     "bin__shelf__rack__zone",
                     ("created_at", admin.DateFieldListFilter)]
    search_fields = ["plan_id", "product__product_name", "product__barcode",
                     "vendor__vendor_name", "batch__batch_number",
                     "bin__bin_id", "grn_item__grn__grn_id"]
    readonly_fields = [
        "plan_id", "grn_item", "product", "vendor", "batch", "bin",
        "planned_quantity", "quantity_placed",
        "status", "completed_by", "completed_at",
        "zone_display", "completion_pct", "created_at",
    ]
    ordering      = ["-created_at"]
    date_hierarchy = "created_at"
    raw_id_fields = []   # plan details are fully read-only

    fieldsets = [
        ("Plan", {
            "fields": ["plan_id", "grn_item", "product", "vendor", "batch"],
        }),
        ("Location", {
            "fields": ["bin", "zone_display"],
        }),
        ("Quantities", {
            "fields": ["planned_quantity", "quantity_placed", "completion_pct"],
        }),
        ("Status", {
            "fields": ["status", "completed_by", "completed_at", "notes"],
        }),
        ("Meta", {
            "classes": ["collapse"],
            "fields": ["created_at"],
        }),
    ]

    def zone_display(self, obj):
        try:
            return f"{obj.bin.shelf.rack.zone.zone_id} ({obj.bin.shelf.rack.zone.zone_type})"
        except Exception:
            return "—"
    zone_display.short_description = "Zone"

    def completion_pct(self, obj):
        if obj.planned_quantity == 0:
            return "0 %"
        pct = round(obj.quantity_placed / obj.planned_quantity * 100, 1)
        colour = "green" if pct >= 100 else ("orange" if pct > 0 else "red")
        return format_html("<span style='color:{};'>{} %</span>", colour, pct)
    completion_pct.short_description = "Complete"

    def has_add_permission(self, request):
        return False

    def has_change_permission(self, request, obj=None):
        return False