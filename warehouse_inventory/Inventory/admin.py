@admin.register(Rack)
class RackAdmin(admin.ModelAdmin):
    list_display  = ["rack_id", "zone", "shelf_count", "bin_count_per_shelf",
                     "bin_capacity", "bin_volume_cm3", "bin_max_weight_kg",
                     "distance_from_dispatch", "created_at"]
    list_filter   = ["zone"]
    search_fields = ["rack_id", "zone__zone_id"]
    readonly_fields = [
        "_shelves_generated", "created_at",
        "shelf_count_actual", "bin_count_total",
    ]
    ordering      = ["rack_id"]

    fieldsets = [
        ("Location", {
            "fields": ["rack_id", "zone", "distance_from_dispatch"],
        }),
        ("Shelf / Bin Template", {
            "description": (
                "These values are used when auto-generating shelves and bins. "
                "shelf_count must be divisible by 3."
            ),
            "fields": [
                "shelf_count", "bin_count_per_shelf",
                "bin_capacity", "bin_max_weight_kg", "bin_volume_cm3",
                "max_weight_kg",
            ],
        }),
        ("Auto-generation Status", {
            "classes": ["collapse"],
            "fields": ["_shelves_generated", "shelf_count_actual", "bin_count_total"],
        }),
        ("Meta", {
            "fields": ["created_at"],
        }),
    ]

    def shelf_count_actual(self, obj):
        return obj.shelves.count()
    shelf_count_actual.short_description = "Shelves Created"

    def bin_count_total(self, obj):
        return Bin.objects.filter(shelf__rack=obj).count()
    bin_count_total.short_description = "Bins Created"


